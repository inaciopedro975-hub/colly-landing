// Reads all -blur.webp files and writes src/lib/blurData.ts with base64 data URLs
import { readFile, readdir, writeFile } from "node:fs/promises";
import { join } from "node:path";

const GALERIA = "public/galeria";
const OUT = "src/lib/blurData.ts";

async function run() {
  const files = (await readdir(GALERIA))
    .filter((f) => f.endsWith("-blur.webp"))
    .sort();

  const entries = await Promise.all(
    files.map(async (f) => {
      const buf = await readFile(join(GALERIA, f));
      const b64 = `data:image/webp;base64,${buf.toString("base64")}`;
      const key = f.replace("-blur.webp", "").replace("-", "_");
      return `  "${key}": "${b64}"`;
    })
  );

  const content = `// Auto-generated by scripts/gen-blur-base64.mjs — do not edit manually
export const blurData: Record<string, string> = {\n${entries.join(",\n")}\n};\n`;

  await writeFile(OUT, content, "utf-8");
  console.log(`✓ Wrote ${entries.length} blur placeholders → ${OUT}`);
}

run().catch((e) => { console.error(e); process.exit(1); });
